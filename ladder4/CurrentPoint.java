package ladder4;

public class CurrentPoint {
	private int pointX;
	private int pointY;

	public CurrentPoint(int pointX, int pointY) {
		this.pointX = pointX;
		this.pointY = pointY;
	}

	public int getPointX() {
		return pointX;
	}

	public void setPointX(int pointX) {
		this.pointX = pointX;
	}

	public int getPointY() {
		return pointY;
	}

	public void setPointY(int pointY) {
		this.pointY = pointY;
	}

}
